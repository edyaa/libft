void	ft_bzero(void *array, int n)
{
	char	*tmp;
	int		i;

	tmp = array;
	if (tmp == (void*)0)
		return ((void*)0);
	i = 0;
	while (i < n)
	{
		tmp[i] = '\0';
		i++;
	}
}

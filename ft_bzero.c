#include "libft.h"

void	ft_bzero(void *array, size_t n)
{
	char	*tmp;
	size_t	i;

	tmp = array;
	if (!tmp)
		return ;
	i = 0;
	while (i < n)
	{
		tmp[i] = 0;
		i++;
	}
}
